siatka = []
# TWORZENIE TLA
C = Canvas(top, bg="white", height=wysokosc, width=szerokosc, bd=0)
for i in range(wysokosc // kratka):
    for j in range(skala):
        siatka.append(C.create_rectangle(j * kratka, i * kratka, (j + 1) * kratka, (i + 1) * kratka, fill="white",
                                         outline="black", width=2))


def pomaluj_punkt(i, kolor):
    C.itemconfig(siatka[i], fill=kolor)

def save():
    output = open("map.txt", mode='w')
    for item in mapa:
        output.write(str(item))
    output.close()

def load():
    global mapa
    print(mapa)
    mapa = load_map()
    print(mapa)
    for i in range(len(mapa)):
        if mapa[i] == 0:
            pomaluj_punkt(i, "white")
        elif mapa[i] == 1:
            pomaluj_punkt(i, "black")
        elif mapa[i] == 2:
            pomaluj_punkt(i, "aqua")
        elif mapa[i] == 3:
            pomaluj_punkt(i, "navy")
        

def load_map():
    map = open("map.txt", 'r')
    mapa = []
    data = map.readline()
    for i in range(len(data)):
        mapa.append(int(data[i]))
    map.close()
    return mapa

def click(event):
    i = (event.x // kratka) + (event.y // kratka) * skala
    if var.get() == 0:
        mapa[i] = 0
        pomaluj_punkt(i, "white")
    elif var.get() == 1:
        mapa[i] = 1
        pomaluj_punkt(i, "black")
    elif var.get() == 2:
        mapa[i] = 2
        pomaluj_punkt(i, "aqua")
    elif var.get() == 3:
        mapa[i] = 3
        pomaluj_punkt(i, "navy")

# Button frame
